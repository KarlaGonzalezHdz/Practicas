/* VISION */
function MostrarOcultar() {
    var Btn = document.getElementById('btn1');
    if (Btn.value == "Mostrar") {
        Mostrar();
        Btn.value = "Ocultar";
    } else {
        Ocultar();
        Btn.value = "Mostrar";
    }

}

function Mostrar() {
    document.getElementById('Lema').style.display = "block";
}

function Ocultar() {
    document.getElementById('Lema').style.display = "none";
}
